defmodule Tasktracker3Web.UserController do
  use Tasktracker3Web, :controller

  alias Tasktracker3.Accounts
  alias Tasktracker3.Accounts.User

  action_fallback Tasktracker3Web.FallbackController

  def index(conn, _params) do
    users = Accounts.list_users()
    render(conn, "index.json", users: users)
  end

  def create(conn, %{"user" => user_params}) do
    user = user_params;
    p = Comeonin.Argon2.hashpwsalt(user["password"])
    with {:ok, %User{} = user} <- Accounts.create_user(%{email: user["email"], name: user["name"], password_hash: p}) do
      conn
      |> put_status(:created)
      |> put_resp_header("location", user_path(conn, :show, user))
      |> render("show.json", user: user)
    end
  end

  def show(conn, %{"id" => id}) do
    user = Accounts.get_user!(id)
    render(conn, "show.json", user: user)
  end

  def update(conn, %{"id" => id, "user" => user_params}) do
    user = Accounts.get_user!(id)

    with {:ok, %User{} = user} <- Accounts.update_user(user, user_params) do
      render(conn, "show.json", user: user)
    end
  end

  def delete(conn, %{"id" => id}) do
    user = Accounts.get_user!(id)
    with {:ok, %User{}} <- Accounts.delete_user(user) do
      send_resp(conn, :no_content, "")
    end
  end
end
